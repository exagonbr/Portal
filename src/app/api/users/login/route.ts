export async function POST(request: NextRequest) {
  try {
    const body = await request.json();
    const { email, password } = body;

    // Valida√ß√£o b√°sica
    if (!email || !password) {
      return NextResponse.json(
        { 
          success: false, 
          message: 'Email e senha s√£o obrigat√≥rios' 
        },
        { 
          status: 400,
          headers: corsHeaders
        }
      );
    }

    // Usar a fun√ß√£o getInternalApiUrl para obter a URL correta do backend
    const backendUrl = getInternalApiUrl('/api/users/login');
    
    console.log('üîê [USERS-LOGIN] Tentativa de login para:', email);
    console.log('üîó [USERS-LOGIN] URL do backend:', backendUrl);

    // Fazer requisi√ß√£o para o backend com timeout
    const controller = new AbortController();
    const timeoutId = setTimeout(() => controller.abort(), 30000); // 30 segundos timeout

    try {
      const response = await fetch(backendUrl, {
        method: 'POST',
        headers: {
          'Content-Type': 'application/json',
          'Accept': 'application/json',
        },
        body: JSON.stringify({ email, password }),
        signal: controller.signal,
      });

      clearTimeout(timeoutId);

      // Verificar se a resposta √© HTML (indicando erro de roteamento)
      const contentType = response.headers.get('content-type');
      if (contentType && contentType.includes('text/html')) {
        console.log('‚ùå [USERS-LOGIN] Recebido HTML em vez de JSON - poss√≠vel erro de roteamento');
        return NextResponse.json(
          { 
            success: false, 
            message: 'Erro de comunica√ß√£o com o servidor de autentica√ß√£o',
            details: {
              error: 'Backend retornou HTML em vez de JSON',
              contentType,
              status: response.status
            }
          },
          { 
            status: 502,
            headers: corsHeaders
          }
        );
      }

      const data = await response.json();

      console.log('üì° [USERS-LOGIN] Resposta do backend:', {
        status: response.status,
        success: data.success,
        hasToken: !!data.data?.token || !!data.token
      });

      if (!response.ok) {
        return NextResponse.json(
          { 
            success: false, 
            message: data.message || 'Erro ao fazer login',
            details: data
          },
          { 
            status: response.status,
            headers: corsHeaders
          }
        );
      }

      // Login bem-sucedido
      return NextResponse.json({
        success: true,
        message: 'Login realizado com sucesso',
        data: {
          accessToken: data.data?.accessToken || data.accessToken,
          refreshToken: data.data?.refreshToken || data.refreshToken,
          user: data.data?.user || data.user,
          expiresIn: data.data?.expiresIn || data.expiresIn
        }
      }, {
        headers: corsHeaders
      });

    } catch (fetchError: any) {
      clearTimeout(timeoutId);
      
      // Verificar se √© um erro de timeout
      if (fetchError.name === 'AbortError') {
        console.log('‚ùå [USERS-LOGIN] Timeout na requisi√ß√£o para o backend');
        return NextResponse.json(
          { 
            success: false, 
            message: 'Timeout na comunica√ß√£o com o servidor de autentica√ß√£o',
            details: {
              error: 'Timeout ap√≥s 30 segundos',
              type: 'AbortError'
            }
          },
          { 
            status: 504,
            headers: corsHeaders
          }
        );
      }
      
      // Verificar se √© um erro de conex√£o recusada (ECONNREFUSED)
      const errorMessage = String(fetchError);
      if (errorMessage.includes('ECONNREFUSED')) {
        console.log('‚ùå [USERS-LOGIN] Conex√£o recusada pelo backend:', errorMessage);
        return NextResponse.json(
          { 
            success: false, 
            message: 'N√£o foi poss√≠vel conectar ao servidor de autentica√ß√£o',
            details: {
              error: 'ECONNREFUSED',
              message: errorMessage
            }
          },
          { 
            status: 503,
            headers: corsHeaders
          }
        );
      }
      
      throw fetchError;
    }

  } catch (error: any) {
    console.log('‚ùå [USERS-LOGIN] Erro no login:', error);
    
    return NextResponse.json(
      { 
        success: false, 
        message: 'Erro interno do servidor',
        details: {
          error: error.message,
          stack: process.env.NODE_ENV === 'development' ? error.stack : undefined
        }
      },
      { 
        status: 500,
        headers: corsHeaders
      }
    );
  }
}

